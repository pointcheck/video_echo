int insert_huffman2(unsigned char* from, int fromLen, unsigned char *to, int *toLen) {
	
	if(fromLen < 2048)
		return -1;

	memcpy(to, jpg_hdr, sizeof(jpg_hdr));
	to+=sizeof(jpg_hdr);
	//memcpy(to, huffman_table, sizeof(huffman_table) - 1);
	//to+=sizeof(huffman_table) - 1;
	memcpy(to, dht_data, DHT_SIZE);
	to+=DHT_SIZE;

	//removing avi header
	//int avi_hdr_size = (*(from + 4) << 8) +  *(from + 5) + 4;
	int avi_hdr_size = 2;
	

	if(avi_hdr_size > 200) {
		*toLen = 0;
		printf("HUFF: avi_hdr_size = %d is too big!\n", avi_hdr_size);
		return -2;
	}

	//printf("HUFF: avi_hdr_size = %d, fromLen = %d\n", avi_hdr_size, fromLen);

	memcpy(to, from + avi_hdr_size, fromLen - avi_hdr_size);

	*toLen = sizeof(jpg_hdr) + 
		DHT_SIZE + 
		(fromLen - avi_hdr_size);

	return 1;
}


void insert_huffman(unsigned char* from, int fromLen, unsigned char *to, int *toLen) {

	*toLen = 0;

	if( *(unsigned short*) from != 0xd8ff)
		return; // wrong format


	*(unsigned int*)(from+6) = 0x4649464A; // "JFIF"

	int has_dht = 0;

	memcpy(to, from, 2);
	from  += 2;
	to    += 2;
	*toLen += 2;
	fromLen -= 2;
	
	while(fromLen > 0 && !has_dht) {
		if(from[0] != 0xff) 
			return;
		if(from[1] == 0xc4)
			has_dht++;
		else if(from[1] == 0xda) {
			fromLen -= 4;
			break;
		}

		// Skip to the next marker

		int size = (from[2] << 8) + from[3];
		memcpy(to, from, 4 + size - 2);
		to += 4 + size - 2;
		from += 4 + size - 2;	
		*toLen += 4 + size - 2;	
		fromLen -= 4 + size - 2;
	}

	if(!has_dht) {
		memcpy(to, huffman_table, sizeof(huffman_table) - 1);
		*toLen += sizeof(huffman_table) - 1;
		to+= sizeof(huffman_table) - 1;
	}

	memcpy(to, from, fromLen);
	*toLen += fromLen;

}


int insert_huffman3(unsigned char* from, int fromLen, unsigned char *to, int *toLen) {

	/* Insert Huffman tables in jpeg picture */

	memcpy(to, from, HEADERFRAME1);
	*(unsigned int*)(to+6) = 0x4649464A; // "JFIF"
	memcpy(to + HEADERFRAME1, dht_data, DHT_SIZE);
	memcpy(to + HEADERFRAME1 + DHT_SIZE, from + HEADERFRAME1, fromLen - HEADERFRAME1);
	*toLen = fromLen + DHT_SIZE;

	return 1;
}

